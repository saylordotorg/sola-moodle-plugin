    /**
     * Look up a translated string.
     *
     * @param {string|null} lang ISO 639-1 code (e.g. 'es', 'zh'), or null for English
     * @param {string}      key  Key defined in EN
     * @return {string} Translated string, or English fallback, or empty string
     */
    const get = function(lang, key) {
        const code = (lang || 'en').substring(0, 2).toLowerCase();
        const pack = LANGS[code];
        if (pack && pack[key] !== undefined && pack[key] !== '') {
            return pack[key];
        }
        if (EN[key] !== undefined) {
            return EN[key];
        }
        return '';
    };

    /**
     * @param {string} lang ISO 639-1 code
     * @return {boolean} true if a pack exists for this language
     */
    const has = function(lang) {
        const code = (lang || 'en').substring(0, 2).toLowerCase();
        return LANGS[code] !== undefined;
    };

    /**
     * @return {Object} Full English table (used by tests / fallbacks)
     */
    const getEn = function() {
        return EN;
    };

    return {
        get:   get,
        has:   has,
        getEn: getEn,
    };
